package pcm.model.shape;

import pcm.geom.Vector3D;

/**
 * {@code Surface} represent <i>walls</i> of the towers.
 * In 2D case, Surface is a face of the polyhedra.
 * In 3D case, Surface is a plane on which 3D structure resides.
 * 
 * @author Satayev
 */
public abstract class Surface {

  // ///////////////////////////////////////////////////////////////////////////
  // Fields
  // ///////////////////////////////////////////////////////////////////////////
  /** Position vector */
  public Vector3D p;
  /** Normal vector */
  public Vector3D n;

  // ///////////////////////////////////////////////////////////////////////////
  // Constructors
  // ///////////////////////////////////////////////////////////////////////////
  public Surface(Vector3D position, Vector3D normal) {
    this.p = position;
    this.n = normal;
  }

  // ///////////////////////////////////////////////////////////////////////////
  // Methods
  // ///////////////////////////////////////////////////////////////////////////

  // ///////////////////////////////////////////////////////////////////////////
  // Getters and Setters
  // ///////////////////////////////////////////////////////////////////////////

}
